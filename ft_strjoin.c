#include "libft.h"

char	*ft_strjoin(char *s1, char *s2)
{
	int		len1 ;
	int		len2 ;
	int		i;
	int		j;
	char	*str;

	str = NULL;
	i = 0 ;
	j = 0 ;
	if (!s1 || !s2)
		return (NULL);
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	str = (char *)malloc((len1 + len2 + 1) * sizeof(char));
	if (!str)
		return (NULL);
	while (s1[j] != '\0')
		str[i++] = s1[j++];
	j = 0;
	while (s2[j] != '\0')
		str[i++] = s2[j++];
	str[i] = '\0';
	return (str);
}
